public class Homework01 {
    public static void main(String[] args) {

       //输出小写的a-z
        for(char c1 = 'a'; c1 <= 'z'; c1++){
            System.out.println(c1);
        }
        System.out.println("====");

        //输出大写的Z-A
        for(char c1 = 'Z'; c1 >= 'A'; c1--){
            System.out.println(c1 + "");
        }
    }
}
